package day_11;
public class Day_25 {
    public static void main(String[] args) {
//Membuat pola persegi menggunakan operator perulangan (Lopping)
        int jumlah = 4;
        for (int nilai1 = 1; nilai1 <= jumlah; nilai1++) {
            for (int nilai2 = 1; nilai2 < 4; nilai2++) {
                System.out.print("o  ");
            }
            System.out.println("o ");
        }
        System.out.println(" ");
    }
    
}
